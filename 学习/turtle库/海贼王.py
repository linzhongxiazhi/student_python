# 海贼王图标
from turtle import *

speed(1000)
pencolor("black")
pensize(300)
goto(-350, 350)
for i in range(2):
    fd(700)
    circle(-100, 180)
    fd(700)
    circle(100, 180)
goto(0, -150)  # 坐标（0，-150）
pencolor("white")
pensize(15)
circle(150)  # 半径150
penup()
goto(0, -200)  # 坐标（0，-200）
pendown()
pensize(20)  # 笔粗20
circle(200, 15)
for i in range(5):
    penup()
    circle(200, 30)
    pendown()
    circle(200, 30)
penup()
circle(200, 30)
pendown()
circle(200, 15)
penup()
goto(-150, 0)
pendown()
seth(0)
fd(60)
x = pow(150 ** 2 - 30 ** 2, 1 / 2)
y = pow(150 ** 2 - 90 ** 2, 1 / 2)
seth(270)
fd(y)
goto(-90, 0)
seth(0)
fd(60)
seth(270)
fd(x)
goto(-30, 0)
seth(0)
fd(60)
seth(270)
fd(x)
goto(30, 0)
seth(0)
fd(60)
seth(270)
fd(y)
goto(90, 0)
seth(0)
fd(60)
penup()
goto(0, 25)
pendown()
circle(6)
penup()
goto(-60, 60)
pensize(29)
pendown()
circle(15)
penup()
goto(60, 60)
pendown()
circle(15)
pensize(20)
penup()
goto(0, -150)
pendown()
seth(270)
fd(45)
penup()
goto(0, 150)
pendown()
seth(90)
fd(45)
z = pow(150 ** 2 - 75 ** 2, 1 / 2)
penup()
goto(z, 75)
pendown()
seth(30)
fd(45)
penup()
goto(z, -75)
pendown()
seth(-30)
fd(45)
penup()
goto(-z, -75)
pendown()
seth(210)
fd(45)
penup()
goto(-z, 75)
pendown()
seth(150)
fd(45)
done()
